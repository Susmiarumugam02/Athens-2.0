# ─── User: Submit Profile Form ────────────────────────────────────────────────

@api_view(['POST'])
@permission_classes([IsAuthenticated])
def complete_profile(request):
    """User submits profile form on first login. Sets approval_status=pending."""
    user = request.user

    if getattr(user, 'role_type', 'user') != 'user':
        return Response({'error': 'Only regular users can complete profile.'}, status=403)

    if not user.is_first_login:
        return Response({'error': 'Profile already completed.'}, status=400)

    name = request.data.get('name', '').strip()
    phone = request.data.get('phone', '').strip()
    department = request.data.get('department', '').strip()
    designation = request.data.get('designation', '').strip()

    if not all([name, phone, department, designation]):
        return Response({'error': 'name, phone, department, designation are required.'}, status=400)

    # Sanitize and validate phone: digits only, 10 digits
    import re
    phone_clean = re.sub(r'[^0-9]', '', phone)
    if not re.fullmatch(r'\d{10}', phone_clean):
        return Response({'error': 'Enter a valid 10-digit mobile number.'}, status=400)

    user.name = name
    user.phone_number = phone_clean
    user.department = department
    user.designation = designation
    user.is_first_login = False
    user.profile_completed = True
    user.profile_submitted_at = timezone.now()
    user.status = 'pending_approval'
    user.profile_status = 'submitted'
    user.workflow_approval_status = 'waiting_admin_approval'
    user.access_level = 'restricted'
    user.approval_status = 'waiting_admin_approval'
    user.save(update_fields=[
        'name', 'phone_number', 'department', 'designation', 'is_first_login',
        'profile_completed', 'profile_submitted_at', 'status', 'profile_status',
        'workflow_approval_status', 'access_level', 'approval_status',
    ])

    return Response({
        'message': 'Profile submitted. Waiting for admin approval.',
        'approval_status': user.approval_status,
    })
